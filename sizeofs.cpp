#include<iostream>

int main(int argc, char const *argv[])
{
    std::cout << "Size of an Char:\t" << sizeof(char);
    std::cout << "Size of an Integer:\t" << sizeof(int);
    std::cout << "Size of an Float:\t\n" << sizeof(double);
    std::cout << "Size of an Unsigned Long Long:\t\n" << sizeof(unsigned long long);
    return 0;
}
